package conditionalStatements;

public class Encapsulation {
	private String name;
	private int age;
	private double accNumbaer;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public double getAccNumbaer() {
		return accNumbaer;
	}

	public void setAccNumbaer(double accNumbaer) {
		this.accNumbaer = accNumbaer;
	}

}
